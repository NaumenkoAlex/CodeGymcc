package com.codegym.task.task04.task0437;


/* 
Triangle of eights

*/

public class Solution {
    public static void main(String[] args) throws Exception {
        for (int i = 10; i > 0; i--) {
            for (int j = i ; j < 11; j++) {
                System.out.print(8);
            }
            System.out.println("");
        }

    }
}
